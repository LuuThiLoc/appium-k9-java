package lesson_lab_07;

public class Dog extends Animal {
    public Dog(String name, int speed) {
        super(name, speed);
    }

    @Override
    public void getAnimalSpeed() {
        super.getAnimalSpeed();
    }

    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public int getSpeed() {
        return super.getSpeed();
    }
}
